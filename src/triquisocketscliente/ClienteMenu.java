    private void formMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseMoved
        Point p = MouseInfo.getPointerInfo().getLocation();
        if(p.y<110){
            this.getContentPane().setBackground(new Color(110-p.y,3,3));
            jLabel1.setForeground(new Color(p.y,0,0));
            jLabel3.setForeground(new Color(p.y,0,0));
        }
        else{
            this.getContentPane().setBackground(Color.black);
            jLabel1.setForeground(new Color(110,3,3));
            jLabel3.setForeground(new Color(110,3,3));
        }

        
    }//GEN-LAST:event_formMouseMoved

