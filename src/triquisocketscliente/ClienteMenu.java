    private void jLabelConectarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelConectarMouseReleased
        String puertoAux =jTextPuerto.getText();
        servidor = jTextServidor.getText();
        if(puertoAux.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "El puerto no puede ser vacio", "Error", 2);
            return;
        }
        if(servidor.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "El servidor no puede ser vacio", "Error", 2);
            return;
        }
        try{
            puerto= Integer.parseInt(puertoAux);
            
            if(puerto>65535||puerto<=0){
                JOptionPane.showMessageDialog(rootPane, "Puerto inalcanzable", "Error", 2);
                jTextPuerto.setText("");
                return;
            }
            //-----------------
            //Conecta con el servidor
            if(!conectado){
                lanzarCliente();
            }
            
            //------
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "El puerto presenta error", "Error", 2);
            jTextPuerto.setText("");
            return;
        }
        
            
        
        
    }//GEN-LAST:event_jLabelConectarMouseReleased
    void lanzarCliente(){
        clienteVista= new ClienteVista(this);
        Thread hilovista = new Thread(clienteVista);
        hilovista.start();
    }
